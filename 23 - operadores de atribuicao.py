"""
Operadores de atribuição
= += -= *= /= //= **= %=
"""
# adição
a = 2
a += 3

print(a)

# subtração
b = 4
b -= 1

print(b)

# multiplicação
c = 2
c *= 3

print(c)

# Divisão de ponto flutuante
d = 8
d /= 2

print(d)

#  Divisão inteira, sem considerar o resto
e = 6
e //= 3

print(e)

# potencia 
f = 10
f **= 2

print(f)

# resto divisão 
g = 8
g %= 3

print(g)