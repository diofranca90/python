# Condicional not in
# not in	Retorna True(verdaeiro) caso o valor não seja encontrado na sequência

nome = "Diorgenes"
print(nome[1]) # indice do nome "Diorgenes"
print("o" not in nome) # Tem "o" em "Diorgenes" False(Falso)
print("a" not in nome) # Não tem "a" em "Diorgenes" True(verdadeiro)


lista = ["Python", 'Academy', "Operadores", 'Condições']
# Verifica se não existe a string dentro da lista
print('SQL' not in lista) # Saída: True