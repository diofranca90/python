# Condicional in
# in	Retorna True(verdaeiro) caso o valor seja encontrado na sequência

nome = "Diorgenes"
print(nome[1]) # indice do nome "Diorgenes"
print("o" in nome) # Tem "0" em "Diorgene" True(verdadeiro)
print("a" in nome) # Não tem "a" em "Diorgene" False(Falso)


lista = ["Python", 'Academy', "Operadores", 'Condições']
# Verifica se existe a string dentro da lista
print('Python' in lista)  # Saída: True

