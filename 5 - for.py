# for sistema de repetição
# x é o nome da variável que vai receber os elemento da repetição

# exemplo sem condição
lista = [1, 2, 3, 4, 5]

for x in lista:
    print(x)

# exemplo com condição
sequencia = [0, 1, 2, 3, 4, 5]

for x in sequencia:
    print(x)
